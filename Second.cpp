#include<iostream>
using namespace std;
int main()
{
    int a,b;
    cout << "Enter first numbers: ";
    cin>>a;
    cout << "Enter second numbers: ";
    cin>>b;
    cout << "Product of " << a << " and " << b << " is " << a * b << endl;
    cout << "Sum of " << a << " and " << b << " is " << a + b << endl;
    cout << "Difference of " << a << " and " << b << " is " << a - b << endl;
}